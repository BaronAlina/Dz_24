package nomer2;

import nomer2.SimpleMath;

public class SimpleMath2 {
    public static void main(String[] args) {
        SimpleMath simpleMath2 = new SimpleMath();
        System.out.println(simpleMath2.sum(6, 7));
        System.out.println(simpleMath2.div(25, 5));
        System.out.println(simpleMath2.dif(63, 58));
        System.out.println(simpleMath2.com(45, 7));
        System.out.println(simpleMath2.sin(90));
        System.out.println(simpleMath2.cos(30));
    }
}
